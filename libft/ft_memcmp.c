#include"libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;
	size_t			i;

	if (n == 0)
		return (0);
	str1 = (unsigned char *) s1;
	str2 = (unsigned char *) s2;
	i = 0;
	while ((i < n - 1) && n != 1)
	{
		if (str1[i] == str2[i])
		{
			i++;
			continue ;
		}
		break ;
	}
	return (str1[i] - str2[i]);
}
