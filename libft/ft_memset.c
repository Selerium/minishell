#include "libft.h"

void	*ft_memset(void *ptr, int value, size_t length)
{
	size_t	i;
	char	*p;

	p = (char *) ptr;
	i = 0;
	while (i < length)
	{
		p[i] = value;
		i++;
	}
	return (ptr);
}
