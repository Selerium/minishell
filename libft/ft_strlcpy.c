#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	if (size == 0)
		return (ft_strlen(src));
	if (size > ft_strlen(src))
		size = ft_strlen(src) + 1;
	while (i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i++] = '\0';
	return (ft_strlen(src));
}
